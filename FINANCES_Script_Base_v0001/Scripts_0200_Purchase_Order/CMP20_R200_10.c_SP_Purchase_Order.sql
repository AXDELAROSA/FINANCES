					ELSE	'X'
			END )		APPROVED,
				--SE OBTIENE LA K_DEL GERENTE PARA OBTENER LA IMAGEN EN EL REPORTE DE LA PO
			(	CASE
					WHEN	EN_NUM_DEPT IN (1)			THEN	(7945)			-- RH					
					WHEN	EN_NUM_DEPT IN (2,5,6,11)	THEN	(22)			-- PRODUCCION
					WHEN	EN_NUM_DEPT IN (3)			THEN	(4475)			-- MATERIALES
